/*

	This code will perform invariant mass analysis on experimental and simulated data with SABRE multiplicity 3

	This code can take experimental data input in the form of the ROOT files generated by RMSRecon/etc/Rachel2Root.cxx
	which simply "ROOT"-ifies the txt files from Rachel Shaffer's sorts (see her thesis here: https://repository.lsu.edu/gradschool_dissertations/6662/)

	This code can also take simulated data output from SABREslicer.cxx located at SABREsim/det/SABREslicer.cxx. This takes SABREsim data and formats
	it in the same format as the Rachel2Root data. 


	NOTE: This code assumes that TMassTable is loaded into ROOT. This can be done by doing one of the following:

		i)		manually running '.L /path/to/masstable.cpp+' everytime you open root

													OR

		ii) 	creating/editing ~/rootlogon.C to include: 'gROOT->ProcessLine(".L /path/to/masstable.cpp+");'
				so that it runs this automatically for you. Find masstable.cpp and associated header and dat file here:
				https://github.com/zmp26/masstable

*/

#include <map>
#include <vector>
#include "TFile.h"
#include "TH1D.h"
#include "TString.h"
#include "TDirectory.h"

std::map<TString, std::map<TString, TH1D*>> hMap;

const double DEGRAD = M_PI / 180.;
const double RADDEG = 180. / M_PI;

void B10ha(const char* input_filename, const char* output_filename, TString assumption="8Be"){

	bool Be8, Li5;
	assumption.ToLower();

	if(assumption.EqualTo("8be")){
		Be8 = true;
		Li5 = false;
	} else if(assumption.EqualTo("5li")){
		Be8 = false;
		Li5 = true;
	} else {
		Be8 = false;
		Li5 = false;
		std::cout << "Unexpected assumption: " << assumption.Data() << "\n";
		return;
	}



	TFile *outfile = new TFile(output_filename, "RECREATE");
	//std::vector<TString> caseNames = {"a1_p_a2", "a1_a2_p", "a2_p_a1", "a2_a1_p", "p_a1_a2", "p_a2_a1", "allCases"};//update with proper assignments: paa, apa, aap, etc
	std::vector<TString> caseNames = {"012", "021", "102", "120", "201", "210", "allCases"};


	//prep the histograms in hMap
	for(auto &cn : caseNames){
		TDirectory *dir = outfile->mkdir(cn);
		dir->cd();

		// 1. Invariant Mass & Excitation Energy Histograms
        hMap[cn]["daughterIM"]       = new TH1D(cn + "_daughterIM", "Daughter Invariant Mass;MeV/c^{2}", 29000, 4600, 7500);
        hMap[cn]["daughterExE"]      = new TH1D(cn + "_daughterExE", "Daughter Ex;MeV", 525, -1, 20);
        hMap[cn]["ReconExE"]         = new TH1D(cn + "_ReconExE", "9B Recon Ex;MeV", 525, -1, 20);
        hMap[cn]["ReconExE_gated"]   = new TH1D(cn + "_ReconExE_gated", "Gated 9B Recon Ex;MeV", 525, -1, 20);

        // 2. Kinematics Histograms (CM Frame)
        // Particle p
        hMap[cn]["pvcm"]             = new TH1D(cn + "_pvcm", "p Velocity CM;c", 100, 0, 1);
        hMap[cn]["pkecm"]            = new TH1D(cn + "_pkecm", "p KE CM;MeV", 100, 0, 1);
        hMap[cn]["pthetacm"]         = new TH1D(cn + "_pthetacm", "p Theta CM;deg", 180, 0, 180);
        hMap[cn]["pphicm"]           = new TH1D(cn + "_pphicm", "p Phi CM;deg", 360, 0, 360);

        // Particle a1
        hMap[cn]["a1vcm"]            = new TH1D(cn + "_a1vcm", "a1 Velocity CM;c", 100, 0, 1);
        hMap[cn]["a1kecm"]           = new TH1D(cn + "_a1kecm", "a1 KE CM;MeV", 100, 0, 1);
        hMap[cn]["a1thetacm"]        = new TH1D(cn + "_a1thetacm", "a1 Theta CM;deg", 180, 0, 180);
        hMap[cn]["a1phicm"]          = new TH1D(cn + "_a1phicm", "a1 Phi CM;deg", 360, 0, 360);

        // Particle a2
        hMap[cn]["a2vcm"]            = new TH1D(cn + "_a2vcm", "a2 Velocity CM;c", 100, 0, 1);
        hMap[cn]["a2kecm"]           = new TH1D(cn + "_a2kecm", "a2 KE CM;MeV", 100, 0, 1);
        hMap[cn]["a2thetacm"]        = new TH1D(cn + "_a2thetacm", "a2 Theta CM;deg", 180, 0, 180);
        hMap[cn]["a2phicm"]          = new TH1D(cn + "_a2phicm", "a2 Phi CM;deg", 360, 0, 360);

        // Daughter specific CM
        hMap[cn]["daughtervcm"]      = new TH1D(cn + "_daughtervcm", "Daughter Velocity CM;c", 100, 0, 1);
        hMap[cn]["daughterkecm"]     = new TH1D(cn + "_daughterkecm", "Daughter KE CM;MeV", 100, 0, 1);
        hMap[cn]["daughterthetacm"]  = new TH1D(cn + "_daughterthetacm", "Daughter Theta CM;deg", 180, 0, 180);
        hMap[cn]["daughterphicm"]    = new TH1D(cn + "_daughterphicm", "Daughter Phi CM;deg", 360, 0, 360);

        // Sequential Decay Energies
        hMap[cn]["ecm1"]             = new TH1D(cn + "_ecm1", "E_{cm} Decay 1;MeV", 200, 0, 20);
        hMap[cn]["ecm2"]             = new TH1D(cn + "_ecm2", "E_{cm} Decay 2;MeV", 200, 0, 20);

		outfile->cd();
	}

	TMassTable fMassTable;
	fMassTable.Init("/mnt/e/masstable/masstable.dat");

	double beamMass, targetMass, ejectileMass, recoilMass, daughterMass, alphaMass, protonMass;

	double gate_Be8 = 0.1;
	double gate_Li5 = 0.2;
	double gate;

	beamMass = fMassTable.GetNuclearMassMeV("He",3);
	targetMass = fMassTable.GetNuclearMassMeV("B",10);
	ejectileMass = fMassTable.GetNuclearMassMeV("He",4);
	recoilMass = fMassTable.GetNuclearMassMeV("B",9);
	alphaMass = fMassTable.GetNuclearMassMeV("He",4);
	protonMass = fMassTable.GetNuclearMassMeV("H",1);

	double masses[3];// = {alphaMass, protonMass, alphaMass};
	struct Perm{ int i, j, k; };
	//perm tells us which hit indices to take from the event
	//the 0th index is always the first decay particle from the reaction recoil
	//the 1st and 2nd indices are always the breakup of the intermediate daughter produced from first decay
	//so {0,1,2} means that for a given event in the TTree we pull out the 0th position data and assume it is the 
	//first decay particle, we pull out the 1st position data and assume it is the second decay particle and
	//the 2nd position data and assume it is the third decay particle. For {2,0,1} we take the 2nd position data to
	//assume it is the first decay particle, the 0th and assume it is the second decay particle, and the 1st and assume
	//the third decay particle. And so on.
	std::map<TString, Perm> pMap = {
		{"012", {0,1,2}},
		{"021", {0,2,1}},
		{"102", {1,0,2}},
		{"120", {1,2,0}},
		{"201", {2,0,1}},
		{"210", {2,1,0}}
	};

	if(Be8){
		daughterMass = fMassTable.GetNuclearMassMeV("Be",8);
		gate = gate_Be8;

		masses[0] = protonMass;//p
		masses[1] = alphaMass;//a1
		masses[2] = alphaMass;//a2
	}

	else if(Li5){
		daughterMass = fMassTable.GetNuclearMassMeV("Li",5);
		gate = gate_Li5;

		masses[0] = alphaMass;//a1
		masses[1] = protonMass;//p
		masses[2] = alphaMass;//a2
	}

	std::cout << "Procesing file " << input_filename << " with daughter assumption " << assumption << std::endl;

	TFile *infile = new TFile(input_filename,"READ");
	TTree *intree = (TTree*)infile->Get("mult3");

	double SPSEnergy, SPSTheta, SPSPhi;
	int SR1, SW1; double SRE1, SWE1, Stheta1, Sphi1;
	int SR2, SW2; double SRE2, SWE2, Stheta2, Sphi2;
	int SR3, SW3; double SRE3, SWE3, Stheta3, Sphi3;

	intree->SetBranchAddress("SPSEnergy",&SPSEnergy);
	intree->SetBranchAddress("SPSTheta",&SPSTheta);
	intree->SetBranchAddress("SPSPhi",&SPSPhi);

	intree->SetBranchAddress("SabreRing_hit1",&SR1);
	intree->SetBranchAddress("SabreWedge_hit1",&SW1);
	intree->SetBranchAddress("SabreRingEnergy_hit1",&SRE1);
	intree->SetBranchAddress("SabreWedgeEnergy_hit1",&SWE1);
	intree->SetBranchAddress("thetalab_hit1",&Stheta1);
	intree->SetBranchAddress("philab_hit1",&Sphi1);

	intree->SetBranchAddress("SabreRing_hit2",&SR2);
	intree->SetBranchAddress("SabreWedge_hit2",&SW2);
	intree->SetBranchAddress("SabreRingEnergy_hit2",&SRE2);
	intree->SetBranchAddress("SabreWedgeEnergy_hit2",&SWE2);
	intree->SetBranchAddress("thetalab_hit2",&Stheta2);
	intree->SetBranchAddress("philab_hit2",&Sphi2);

	intree->SetBranchAddress("SabreRing_hit3",&SR3);
	intree->SetBranchAddress("SabreWedge_hit3",&SW3);
	intree->SetBranchAddress("SabreRingEnergy_hit3",&SRE3);
	intree->SetBranchAddress("SabreWedgeEnergy_hit3",&SWE3);
	intree->SetBranchAddress("thetalab_hit3",&Stheta3);
	intree->SetBranchAddress("philab_hit3",&Sphi3);

	long nentries = intree->GetEntries();
	for(long entry=0; entry<nentries; entry++){

		intree->GetEntry(entry);

		for(auto const& [name, p] : pMap){

			TLorentzVector lv[3];
			int indices[3] = {p.i, p.j, p.k};

			double SRE[3] = {SRE1, SRE2, SRE3};
			double Stheta[3] = {Stheta1, Stheta2, Stheta3};
			double Sphi[3] = {Sphi1, Sphi2, Sphi3};

			//this loop iterates through indices (which contains our pMap permutation) and assigns lv[n]
			//to indices[n] kinematic information
			for(int n=0; n<3; n++){
				int hitIdx = indices[n];//which sabre "hit" to use
				double mass = masses[n];//which mass to assign it to
				double mom = std::sqrt(2*mass*SRE[hitIdx]);
				lv[n].SetPxPyPzE(
					mom * std::sin(Stheta[hitIdx]*DEGRAD) * std::cos(Sphi[hitIdx]*DEGRAD),
					mom * std::sin(Stheta[hitIdx]*DEGRAD) * std::sin(Sphi[hitIdx]*DEGRAD),
					mom * std::cos(Stheta[hitIdx]*DEGRAD),
					SRE[hitIdx] + mass
				);
			}

			TLorentzVector daughter, recoil;
			// if(Be8) daughter = lv[1] + lv[2];//a1 + a2
			// else daughter = lv[0] + lv[1];//p + a2
			// recoil = lv[0] + lv[1] + lv[2];
			daughter = lv[1] + lv[2]; //we assume in both cases that lv[0] maps to first decay particle and lv[1], lv[2] to last decay particles
			recoil = lv[0] + lv[1] + lv[2];

			double daughterEx = daughter.M() - daughterMass;
			double recoilEx = recoil.M() - recoilMass;

			hMap[name]["daughterIM"]->Fill(daughter.M());
			hMap[name]["daughterExE"]->Fill(daughterEx);
			hMap[name]["ReconExE"]->Fill(recoilEx);
			hMap["allCases"]["daughterIM"]->Fill(daughter.M());
			hMap["allCases"]["daughterExE"]->Fill(daughterEx);
			hMap["allCases"]["ReconExE"]->Fill(recoilEx);

			//add gated reconexe here eventually!


			//sequential decay CM steps
			//step 1 is 9B -> (8Be + p) || (5Li + a)
			//analyze in CM frame of 9B
			TVector3 boost1 = -recoil.BoostVector();
			TVector3 boost2 = -daughter.BoostVector();

			//TLorentzVector bu1 = Be8 ? lv[0] : lv[2];
			TLorentzVector bu1 = lv[0];
			bu1.Boost(boost1);
			daughter.Boost(boost1);

			//daughter already assigned by case:
			double daughtervcm = ((1/daughter.Energy())*daughter.Vect()).Mag();
			double daughterkecm = 0.5*daughterMass*daughtervcm*daughtervcm;
			double daughterthetacm = RADDEG*acos(daughter.Vect().Z()/daughter.Vect().Mag());
			double daughterphicm = RADDEG*atan2(daughter.Vect().Y(), daughter.Vect().X());
			if(daughterphicm < 0) daughterphicm += 360.;

			hMap[name]["daughtervcm"]->Fill(daughtervcm);
			hMap[name]["daughterkecm"]->Fill(daughterkecm);
			hMap[name]["daughterthetacm"]->Fill(daughterthetacm);
			hMap[name]["daughterphicm"]->Fill(daughterphicm);

			hMap["allCases"]["daughtervcm"]->Fill(daughtervcm);
			hMap["allCases"]["daughterkecm"]->Fill(daughterkecm);
			hMap["allCases"]["daughterthetacm"]->Fill(daughterthetacm);
			hMap["allCases"]["daughterphicm"]->Fill(daughterphicm);

			if(Be8){
				//for Be8 case, proton is bu1 since 9B = p + 8Be
				//so we fill the proton histograms using bu1 info
				double pvcm = ((1/bu1.Energy())*bu1.Vect()).Mag();
				double pkecm = 0.5*protonMass*pvcm*pvcm;
				double pthetacm = RADDEG*acos(bu1.Vect().Z()/bu1.Vect().Mag());
				double pphicm = RADDEG*atan2(bu1.Vect().Y(), bu1.Vect().X());
				if(pphicm < 0) pphicm += 360.;

				hMap[name]["pvcm"]->Fill(pvcm);
				hMap[name]["pkecm"]->Fill(pkecm);
				hMap[name]["pthetacm"]->Fill(pthetacm);
				hMap[name]["pphicm"]->Fill(pphicm);

				hMap["allCases"]["pvcm"]->Fill(pvcm);
				hMap["allCases"]["pkecm"]->Fill(pkecm);
				hMap["allCases"]["pthetacm"]->Fill(pthetacm);
				hMap["allCases"]["pphicm"]->Fill(pphicm);

				double ecm1 = pkecm + daughterkecm;
				hMap[name]["ecm1"]->Fill(ecm1);
				hMap["allCases"]["ecm1"]->Fill(ecm1);

			} else {
				//for Li5 case, alpha1 is bu1 since 9B = a + 5Li
				//so we fill the alpha1 histograms using bu1 info
				double a1vcm = ((1/bu1.Energy())*bu1.Vect()).Mag();
				double a1kecm = 0.5*alphaMass*a1vcm*a1vcm;
				double a1thetacm = RADDEG*acos(bu1.Vect().Z()/bu1.Vect().Mag());
				double a1phicm = RADDEG*atan2(bu1.Vect().Y(), bu1.Vect().X());
				if(a1phicm < 0) a1phicm += 360.;

				hMap[name]["a1vcm"]->Fill(a1vcm);
				hMap[name]["a1kecm"]->Fill(a1kecm);
				hMap[name]["a1thetacm"]->Fill(a1thetacm);
				hMap[name]["a1phicm"]->Fill(a1phicm);

				hMap["allCases"]["a1vcm"]->Fill(a1vcm);
				hMap["allCases"]["a1kecm"]->Fill(a1kecm);
				hMap["allCases"]["a1thetacm"]->Fill(a1thetacm);
				hMap["allCases"]["a1phicm"]->Fill(a1phicm);

				double ecm1 = a1kecm + daughterkecm;
				hMap[name]["ecm1"]->Fill(ecm1);
				hMap["allCases"]["ecm1"]->Fill(ecm1);

			}

			//TLorentzVector bu2 = Be8 ? lv[1] : lv[0];
			//TLorentzVector bu3 = Be8 ? lv[2] : lv[1];
			TLorentzVector bu2 = lv[1];
			TLorentzVector bu3 = lv[2];
			bu2.Boost(boost2);
			bu3.Boost(boost2);

			//since bu3 is always alpha2 in both the 8Be and the 5Li masses definitions, let's fill that first:
			double a2vcm = ((1/bu3.Energy())*bu3.Vect()).Mag();
			double a2kecm = 0.5*alphaMass*a2vcm*a2vcm;
			double a2thetacm = RADDEG*acos(bu3.Vect().Z()/bu3.Vect().Mag());
			double a2phicm = RADDEG*atan2(bu3.Vect().Y(), bu3.Vect().X());
			if(a2phicm < 0) a2phicm += 360.;

			hMap[name]["a2vcm"]->Fill(a2vcm);
			hMap[name]["a2kecm"]->Fill(a2kecm);
			hMap[name]["a2thetacm"]->Fill(a2thetacm);
			hMap[name]["a2phicm"]->Fill(a2phicm);

			hMap["allCases"]["a2vcm"]->Fill(a2vcm);
			hMap["allCases"]["a2kecm"]->Fill(a2kecm);
			hMap["allCases"]["a2thetacm"]->Fill(a2thetacm);
			hMap["allCases"]["a2phicm"]->Fill(a2phicm);

			if(Be8){
				//for Be8 case, bu2 is alpha1
				//this is because 9B-> p + 8Be -> a1 + a2
				double a1vcm = ((1/bu2.Energy())*bu2.Vect()).Mag();
				double a1kecm = 0.5*alphaMass*a1vcm*a1vcm;
				double a1thetacm = RADDEG*acos(bu2.Vect().Z()/bu2.Vect().Mag());
				double a1phicm = RADDEG*atan2(bu2.Vect().Y(), bu2.Vect().X());
				if(a1phicm < 0) a1phicm += 360.;

				hMap[name]["a1vcm"]->Fill(a1vcm);
				hMap[name]["a1kecm"]->Fill(a1kecm);
				hMap[name]["a1thetacm"]->Fill(a1thetacm);
				hMap[name]["a1phicm"]->Fill(a1phicm);

				hMap["allCases"]["a1vcm"]->Fill(a1vcm);
				hMap["allCases"]["a1kecm"]->Fill(a1kecm);
				hMap["allCases"]["a1thetacm"]->Fill(a1thetacm);
				hMap["allCases"]["a1phicm"]->Fill(a1phicm);

				double ecm2 = a1kecm + a2kecm;
				hMap[name]["ecm2"]->Fill(ecm2);
				hMap["allCases"]["ecm2"]->Fill(ecm2);

			} else {
				//for Li5 case, bu2 is proton
				//this is because 9B -> a1 + 5Li + p + a2
				double pvcm = ((1/bu2.Energy())*bu2.Vect()).Mag();
				double pkecm = 0.5*protonMass*pvcm*pvcm;
				double pthetacm = RADDEG*acos(bu2.Vect().Z()/bu2.Vect().Mag());
				double pphicm = RADDEG*atan2(bu2.Vect().Y(), bu2.Vect().X());
				if(pphicm < 0) pphicm += 360.;

				hMap[name]["pvcm"]->Fill(pvcm);
				hMap[name]["pkecm"]->Fill(pkecm);
				hMap[name]["pthetacm"]->Fill(pthetacm);
				hMap[name]["pphicm"]->Fill(pphicm);

				hMap["allCases"]["pvcm"]->Fill(pvcm);
				hMap["allCases"]["pkecm"]->Fill(pkecm);
				hMap["allCases"]["pthetacm"]->Fill(pthetacm);
				hMap["allCases"]["pphicm"]->Fill(pphicm);

				double ecm2 = pkecm + a2kecm;
				hMap[name]["ecm2"]->Fill(ecm2);
				hMap["allCases"]["ecm2"]->Fill(ecm2);
			}



		}


	}

	outfile->Write();
	outfile->Close();
	std::cout << "Analysis finished! Results in " << output_filename << std::endl;


}
